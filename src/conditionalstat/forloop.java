package conditionalstat;

public class forloop 
{
	public static void main(String[] args) 
	{
		for(int i=1;i<=10;i++) 
		{
			System.out.println(i+"*"+"2"+"="+(i*2));
			
		}
		System.out.println("");
		int fact=1;
		for(int i=1;i<=10;i++) 
		{
			fact=fact*i;
			System.out.println(fact);
			
		}
		
	}
}
